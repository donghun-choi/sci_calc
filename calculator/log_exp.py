# calculator/log_exp.py
import math

def log10(x): return math.log10(x)          # Base-10 logarithm
def logx(x, base): return math.log(x, base) # Logarithm with custom base
def square(x): return x ** 2                # Square of x
def power(x, y): return x ** y              # x raised to the power of y
